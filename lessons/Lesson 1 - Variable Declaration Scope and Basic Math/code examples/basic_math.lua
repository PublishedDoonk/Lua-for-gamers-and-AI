-- Now that you can define variables you can do some basic math.
-- You can assign variables to numbers and then perform operations on them,
-- or you can perform operations directly on numbers and save the answer as 
-- a variable.

-- Addition
local a = 5
local b = 10
local sum = a + b

print("a + b =", sum)

-- Subtraction
local difference = a - b

print("a - b =", difference)

-- Multiplication
local product = a * b

print("a * b =", product)

-- Division

local quotient = a / b

print("a / b =", quotient)