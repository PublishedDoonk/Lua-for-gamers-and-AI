-- In Lua, you don't have to specify the type of a variable, but different variables 
-- have different types depending on the value they are assigned.

-- Below are a few examples of different types.

-- Boolean values can be true or false.
local isTrue = true
local isFalse = false
print(isTrue, isFalse)

-- Numbers can be integers (whole numbers) or floating point numbers (decimals).
local anInteger = 42
local aFloat = 3.14
print(anInteger, aFloat)

-- Strings are strings of characters enclosed in quotes. They can be single or double quotes.

local name = "Alice"
local anotherString = "asdfjghga"
print(name, anotherString)
